# What Kind of Comments Should You Write?

# 1. Main Purpose of Comments:
#    - Explain what your code does and how it works.
#    - Help you or others understand the code later, especially after a long break.

# 2. Comments Save Time:
#    - Summarize your overall approach clearly.
#    - Make it easier to recall your logic without rereading all the code.

# 3. Professional Practice:
#    - Writing meaningful comments is a key habit for professional programmers.
#    - Most software is written collaboratively, so others need to understand your code.

# 4. Start Early:
#    - Begin writing useful comments now to build a good habit.
#    - Skilled programmers expect to see comments in code.

# 5. Clear and Concise:
#    - Comments should be short, clear, and to the point.
#    - Avoid overly long or vague comments.

# 6. When to Write a Comment:
#    - If solving a problem required deep thinking or several steps, write a comment about your approach.
#    - It's easier to delete unnecessary comments than to add missing ones later.

